/**
 * Builds a lookup object from the raw dictionary string.
 * Key: lowercase English word, Value: Hebrew translation.
 * Called once at module load — O(n) build, O(1) lookup thereafter.
 */
const buildLocalDatabase = () => {
  const db = {};
  const lines = rawDictionary.trim().split('\n');
  lines.forEach(line => {
    let hebrewIdx = -1;
    for (let i = 0; i < line.length; i++) {
      const code = line.charCodeAt(i);
      if (code >= 1424 && code <= 1535) { hebrewIdx = i; break; }
    }
    if (hebrewIdx !== -1) {
      const english = line.substring(0, hebrewIdx).trim().toLowerCase();
      const hebrew = line.substring(hebrewIdx).trim();
      if (english) db[english] = hebrew;
    }
  });
  return db;
};

export const psychometricDB = buildLocalDatabase();
export default psychometricDB;
